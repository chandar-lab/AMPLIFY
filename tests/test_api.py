def test__compare_sequences_to_out_of_sample__executes_as_expected():

    target_count = 10

    out_of_sample_data_path = os.path.join(
        this_dir, "example-data/easy-out-of-sample.csv"
    )
    with open(out_of_sample_data_path, "r") as f:
        reader = csv.reader(f)
        out_of_sample_sequences = [x[1] for x in reader]

    target_sequence_path = os.path.join(this_dir, "example-data/big-easy-val.csv")
    with open(target_sequence_path, "r") as f0:
        reader0 = csv.reader(f0)

        target_sequences = list()
        for _ in range(target_count):
            target_sequences.append(next(reader0)[1])

    if torch.cuda.is_available():
        device = torch.device("cuda")
    else:
        device = torch.device("cpu")

    model, tokenizer = amplify.AMPLIFY.load(checkpoint_file, config_path)
    easy_regex_result = amplify.inference.compare_sequences_to_out_of_sample_average(
        tokenizer=tokenizer,
        model=model,
        out_of_sample_sequences=out_of_sample_sequences,
        target_sequences=target_sequences,
        device=device,
    )

    assert target_count == len(easy_regex_result)
